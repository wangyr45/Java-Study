package Operator;

public class OperatorDemo07 {
	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;
		System.out.println("a || b = " + (a||b));	//执行或操作
		System.out.println("a | b = "+(a|b));	//执行或操作
		System.out.println("a&&b = "+(a&&b));	//执行与操作
		System.out.println("a&b = "+(a&b));	//执行与操作
		
	}
}
