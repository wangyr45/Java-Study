package Operator;

//一元运算符
public class OperatorDemo02 {
	public static void main(String[] args) {
		boolean b = false;	//定义boolean型数据
		int x = 10;	//定义一个整数
		int y = -30;	//定义一个负数
		System.out.println("b = " + b + ",!b=" + !b);	//取反
		System.out.println("x = " + x + ",-x=" + -x);	//使用负号
		System.out.println("y = " + y + ",+y=" + +y);	//使用正号
		
	}
}
