package DataDemo;

public class DataDemo08 {
	public static void main(String[] args) {
		int x = 30;	//定义整形变量
		float y = 22.19f;	//定义浮点型变量
		System.out.println("x/y=" + (x/y));	//除法操作
		System.out.println("10/3.5 = " + (10/3.5));	//直接使用常量进行除法
		System.out.println("10/3 = " + (10/3));	//直接使用常量进行除法
		
	}
}
