package DataDemo;

public class DataDemo02 {
	public static void main(String[] args) {
		int max = Integer.MAX_VALUE;	//得到整形最大值
		System.out.println("整形的最大值："+max);	//输出最大值
		System.out.println("整形的最大值 + 1"+ (max+1));	//输出最大值+1
		System.out.println("整形的最大值 + 2" + (max+2));	//输出最大值+2
		
		
	}
}
//Integer既可以求出整型最大值和最小值
